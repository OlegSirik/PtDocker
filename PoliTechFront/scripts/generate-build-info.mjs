import { readFileSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const root = join(dirname(fileURLToPath(import.meta.url)), '..');
const pkg = JSON.parse(readFileSync(join(root, 'package.json'), 'utf8'));
const builtAt = new Date().toISOString();

const content = `// Auto-generated by scripts/generate-build-info.mjs — do not edit
export const BUILD_INFO = {
  version: ${JSON.stringify(pkg.version)},
  builtAt: ${JSON.stringify(builtAt)},
} as const;
`;

writeFileSync(join(root, 'src', 'build-info.ts'), content, 'utf8');
console.log(`build-info.ts updated: ${builtAt}`);
